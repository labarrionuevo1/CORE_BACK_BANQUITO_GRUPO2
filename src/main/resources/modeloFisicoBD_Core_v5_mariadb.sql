-- 5. DOMINIO TRANSACCIONAL
-- ============================================================================

CREATE TABLE SUBTIPO_TRANSACCION (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador del subtipo de transaccion',
    CODIGO VARCHAR(30) NOT NULL COMMENT 'Codigo funcional del subtipo',
    NOMBRE VARCHAR(100) NOT NULL COMMENT 'Nombre descriptivo del movimiento',
    TIPO_MOVIMIENTO_BASE VARCHAR(15) NULL COMMENT 'DEBITO o CREDITO si el subtipo aplica a uno en particular',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado del subtipo',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de creacion',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del subtipo de transaccion',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_SUBTIPO_TRANSACCION PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Catalogo dinamico de conceptos de movimientos financieros';

CREATE TABLE TRANSACCION_CUENTA (
    ID BIGINT AUTO_INCREMENT NOT NULL COMMENT 'Identificador unico del movimiento de cuenta de cliente',
    CUENTA_ID INT NOT NULL COMMENT 'Cuenta de cliente afectada',
    SUBTIPO_TRANSACCION_ID INT NOT NULL COMMENT 'Concepto del movimiento',
    UUID_TRANSACCION CHAR(36) NOT NULL COMMENT 'Llave de idempotencia enviada por canal o Switch',
    UUID_GRUPO_OPERACION CHAR(36) NULL COMMENT 'Agrupador de operacion para debito/credito relacionados',
    FECHA_NEGOCIO DATE NOT NULL COMMENT 'Fecha contable o fecha de negocio de la transaccion',
    TIPO_MOVIMIENTO VARCHAR(15) NOT NULL COMMENT 'DEBITO o CREDITO',
    MONTO DECIMAL(19,2) NOT NULL COMMENT 'Valor del movimiento',
    SALDO_RESULTANTE DECIMAL(19,2) NOT NULL COMMENT 'Saldo contable posterior al movimiento',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'COMPLETADA' COMMENT 'Estado del movimiento',
    CANAL_ORIGEN VARCHAR(20) NOT NULL DEFAULT 'CORE' COMMENT 'Canal origen: CORE, AGENCIA, WEB, SWITCH, ATM, SISTEMA',
    REFERENCIA_EXTERNA VARCHAR(100) NULL COMMENT 'Referencia externa, archivo, lote o linea de Switch',
    DESCRIPCION VARCHAR(500) NULL COMMENT 'Descripcion o concepto de la transaccion',
    USUARIO_CORE_ID INT NULL COMMENT 'Usuario interno ejecutor si aplica',
    CREDENCIAL_WEB_ID INT NULL COMMENT 'Credencial web ejecutora si aplica',
    FECHA_TRANSACCION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha y hora real del movimiento',
    NUMERO_COMPROBANTE VARCHAR(20) NULL COMMENT 'Numero de comprobante o voucher',
    CONSTRAINT PK_TRANSACCION_CUENTA PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Bitacora inmutable de movimientos financieros sobre cuentas de clientes';

