-- ============================================================================
-- 4. DOMINIO DE CUENTAS DE CLIENTE E INTERNAS
-- ============================================================================

CREATE TABLE SUBTIPO_CUENTA (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador del producto pasivo',
    TIPO_BASE VARCHAR(15) NOT NULL COMMENT 'Naturaleza base: AHORROS o CORRIENTE',
    CODIGO VARCHAR(20) NOT NULL COMMENT 'Codigo unico del producto',
    NOMBRE VARCHAR(80) NOT NULL COMMENT 'Nombre comercial del producto',
    DESCRIPCION VARCHAR(255) NULL COMMENT 'Descripcion del producto',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado de comercializacion',
    OBSERVACIONES VARCHAR(255) NULL COMMENT 'Reglas u observaciones del producto',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de creacion',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del subtipo de cuenta',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_SUBTIPO_CUENTA PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Catalogo de productos pasivos ofrecidos por el banco';

CREATE TABLE CUENTA (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador unico de la cuenta',
    NUMERO_CUENTA VARCHAR(20) NOT NULL COMMENT 'Numero de cuenta bancaria',
    CLIENTE_ID INT NOT NULL COMMENT 'Titular legal de la cuenta',
    SUCURSAL_ID INT NOT NULL COMMENT 'Sucursal de apertura',
    SUBTIPO_CUENTA_ID INT NOT NULL COMMENT 'Producto contratado',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVA' COMMENT 'Estado operativo de la cuenta',
    SALDO_CONTABLE DECIMAL(19,2) NOT NULL DEFAULT 0.00 COMMENT 'Saldo real contable de la cuenta',
    SALDO_DISPONIBLE DECIMAL(19,2) NOT NULL DEFAULT 0.00 COMMENT 'Saldo utilizable, considerando bloqueos y reglas',
    PERMITE_SOBREGIRO TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'Permite sobregiro controlado para casos autorizados',
    LIMITE_SOBREGIRO DECIMAL(19,2) NOT NULL DEFAULT 0.00 COMMENT 'Limite maximo de sobregiro permitido',
    ES_FAVORITA_PAGOS TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'Cuenta favorita para pagos masivos por SFTP',
    FECHA_APERTURA DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de apertura',
    FECHA_ULTIMO_MOVIMIENTO DATETIME NULL COMMENT 'Fecha del ultimo movimiento financiero',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_CUENTA PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Cuentas pasivas pertenecientes a clientes del banco';

CREATE TABLE CUENTA_INSTITUCIONAL (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador de cuenta interna del banco',
    NUMERO_CUENTA VARCHAR(20) NOT NULL COMMENT 'Numero o codigo interno contable',
    CODIGO VARCHAR(50) NOT NULL COMMENT 'Codigo funcional, ejemplo INGRESOS_SERVICIOS_MASIVOS',
    NOMBRE VARCHAR(100) NOT NULL COMMENT 'Nombre de la cuenta institucional',
    TIPO_CUENTA VARCHAR(20) NOT NULL COMMENT 'INGRESO, PASIVO, IMPUESTO u OPERATIVA',
    SALDO_CONTABLE DECIMAL(19,2) NOT NULL DEFAULT 0.0000 COMMENT 'Saldo acumulado interno',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVA' COMMENT 'Estado de la cuenta interna',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de creacion',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_CUENTA_INSTITUCIONAL PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Cuentas internas del banco para ingresos, impuestos y otros conceptos';

