-- ============================================================================
-- 6. BLOQUEOS, HISTORIAL Y AUDITORIA
-- ============================================================================

CREATE TABLE BLOQUEO_CUENTA (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador del bloqueo o retencion',
    CUENTA_ID INT NOT NULL COMMENT 'Cuenta afectada por el bloqueo',
    MONTO_BLOQUEADO DECIMAL(19,2) NOT NULL COMMENT 'Valor retenido o bloqueado',
    MOTIVO VARCHAR(100) NOT NULL COMMENT 'Motivo del bloqueo',
    AUTORIDAD_ORDENANTE VARCHAR(200) NULL COMMENT 'Autoridad u origen de la orden',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'ACTIVO o LIBERADO',
    USUARIO_CORE_ID INT NOT NULL COMMENT 'Usuario que registra el bloqueo',
    FECHA_BLOQUEO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de registro del bloqueo',
    FECHA_LIBERACION DATETIME NULL COMMENT 'Fecha de liberacion si aplica',
    OBSERVACIONES VARCHAR(300) NULL COMMENT 'Observaciones complementarias',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del bloqueo',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_BLOQUEO_CUENTA PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Bloqueos o retenciones que explican el saldo disponible';

CREATE TABLE HISTORIAL_ESTADO_CUENTA (
    ID BIGINT AUTO_INCREMENT NOT NULL COMMENT 'Identificador del historial de estado',
    CUENTA_ID INT NOT NULL COMMENT 'Cuenta cuyo estado cambia',
    ESTADO_ANTERIOR VARCHAR(15) NULL COMMENT 'Estado anterior de la cuenta',
    ESTADO_NUEVO VARCHAR(15) NOT NULL COMMENT 'Estado nuevo de la cuenta',
    MOTIVO_CAMBIO VARCHAR(300) NULL COMMENT 'Motivo del cambio de estado',
    USUARIO_CORE_ID INT NOT NULL COMMENT 'Usuario que ejecuta el cambio',
    FECHA_CAMBIO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha del cambio',
    CONSTRAINT PK_HISTORIAL_ESTADO_CUENTA PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Trazabilidad de cambios de estado de cuenta';

CREATE TABLE AUDITORIA_EVENTO (
    ID BIGINT AUTO_INCREMENT NOT NULL COMMENT 'Identificador del evento auditado',
    USUARIO_CORE_ID INT NULL COMMENT 'Usuario interno asociado si aplica',
    CREDENCIAL_WEB_ID INT NULL COMMENT 'Credencial web asociada si aplica',
    MODULO VARCHAR(50) NOT NULL COMMENT 'Modulo donde ocurre el evento',
    ACCION VARCHAR(80) NOT NULL COMMENT 'Accion ejecutada',
    ENTIDAD VARCHAR(80) NOT NULL COMMENT 'Entidad afectada',
    ENTIDAD_ID VARCHAR(80) NULL COMMENT 'Identificador de la entidad afectada',
    RESULTADO VARCHAR(20) NOT NULL DEFAULT 'EXITOSO' COMMENT 'Resultado del evento',
    CANAL_ORIGEN VARCHAR(20) NOT NULL DEFAULT 'CORE' COMMENT 'Canal origen del evento',
    IP_ORIGEN VARCHAR(45) NULL COMMENT 'Direccion IP origen si aplica',
    DETALLE_JSON LONGTEXT NULL COMMENT 'Detalle estructurado del evento en formato JSON',
    FECHA_EVENTO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha y hora del evento',
    CONSTRAINT PK_AUDITORIA_EVENTO PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Auditoria transversal de acciones criticas del Core';

