CREATE DATABASE IF NOT EXISTS banquito_core
    DEFAULT CHARACTER SET utf8mb4
    DEFAULT COLLATE utf8mb4_unicode_ci;

USE banquito_core;

SET FOREIGN_KEY_CHECKS = 0;

DROP TABLE IF EXISTS AUDITORIA_EVENTO;
DROP TABLE IF EXISTS HISTORIAL_ESTADO_CUENTA;
DROP TABLE IF EXISTS BLOQUEO_CUENTA;
DROP TABLE IF EXISTS TRANSACCION_INSTITUCIONAL;
