    CONSTRAINT PK_SUBTIPO_CLIENTE PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Catalogo para segmentacion comercial de clientes';

CREATE TABLE CLIENTE (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador unico del cliente',
    SUBTIPO_CLIENTE_ID INT NOT NULL COMMENT 'Segmento comercial del cliente',
    TIPO_CLIENTE VARCHAR(15) NOT NULL COMMENT 'NATURAL o JURIDICO',
    TIPO_IDENTIFICACION VARCHAR(15) NOT NULL COMMENT 'CEDULA, RUC o PASAPORTE',
    IDENTIFICACION VARCHAR(20) NOT NULL COMMENT 'Numero del documento de identidad',
    NOMBRES VARCHAR(100) NULL COMMENT 'Nombres del cliente natural',
    APELLIDOS VARCHAR(100) NULL COMMENT 'Apellidos del cliente natural',
    RAZON_SOCIAL VARCHAR(150) NULL COMMENT 'Razon social del cliente juridico',
    FECHA_NACIMIENTO DATE NULL COMMENT 'Fecha de nacimiento para persona natural',
    FECHA_CONSTITUCION DATE NULL COMMENT 'Fecha de constitucion para persona juridica',
    REPRESENTANTE_LEGAL_ID INT NULL COMMENT 'Cliente natural representante legal de una persona juridica',
    EMAIL VARCHAR(100) NOT NULL COMMENT 'Correo electronico principal',
    TELEFONO_MOVIL VARCHAR(20) NOT NULL COMMENT 'Telefono movil principal',
    DIRECCION VARCHAR(255) NOT NULL COMMENT 'Direccion fisica principal',
    LATITUD DECIMAL(10,8) NULL COMMENT 'Latitud geografica',
    LONGITUD DECIMAL(11,8) NULL COMMENT 'Longitud geografica',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado del cliente',
    ACTIVO_PAGOS_MASIVOS TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'Indica si el cliente juridico tiene habilitado el servicio de pagos masivos',
    FECHA_REGISTRO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de registro del cliente',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del cliente',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_CLIENTE PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Titulares de productos bancarios: personas naturales y juridicas';

