-- ============================================================================
-- 1. DOMINIO DE SUCURSALES, PARAMETROS Y CALENDARIO
-- ============================================================================

CREATE TABLE SUCURSAL (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador unico de la sucursal',
    CODIGO_SUCURSAL VARCHAR(10) NOT NULL COMMENT 'Codigo alfanumerico unico de la sucursal',
    NOMBRE VARCHAR(100) NOT NULL COMMENT 'Nombre comercial de la sucursal',
    CIUDAD VARCHAR(80) NOT NULL COMMENT 'Ciudad donde opera la sucursal',
    DIRECCION VARCHAR(300) NULL COMMENT 'Direccion fisica de la sucursal',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVA' COMMENT 'Estado operativo de la sucursal',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de creacion del registro',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del registro',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_SUCURSAL PRIMARY KEY (ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Catalogo de sucursales del banco';

CREATE TABLE FERIADO (
    FECHA_FERIADO DATE NOT NULL COMMENT 'Fecha exacta del feriado o dia no habil',
    NOMBRE VARCHAR(100) NOT NULL COMMENT 'Nombre o motivo del feriado',
    ES_FIN_SEMANA TINYINT(1) NOT NULL DEFAULT 0 COMMENT '1=fin de semana, 0=feriado configurado',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado de la configuracion',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de configuracion',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del feriado',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_FERIADO PRIMARY KEY (FECHA_FERIADO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Calendario de dias no habiles consultable por Core y Switch';

CREATE TABLE PARAMETRO_CORE (
    CODIGO VARCHAR(50) NOT NULL COMMENT 'Codigo unico del parametro, ejemplo IVA_PORCENTAJE',
    NOMBRE VARCHAR(100) NOT NULL COMMENT 'Nombre legible del parametro',
    VALOR_TEXTO VARCHAR(255) NOT NULL COMMENT 'Valor almacenado como texto para conversion por aplicacion',
    TIPO_DATO VARCHAR(20) NOT NULL COMMENT 'Tipo real del dato: NUMERICO, CADENA, FECHA, HORA, BOOLEANO',
    DESCRIPCION VARCHAR(255) NULL COMMENT 'Descripcion funcional del parametro',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima modificacion',
    VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para control de concurrencia optimista JPA',
    CONSTRAINT PK_PARAMETRO_CORE PRIMARY KEY (CODIGO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Parametros dinamicos del Core Bancario';

-- ============================================================================
-- 2. DOMINIO DE CLIENTES
-- ============================================================================

CREATE TABLE SUBTIPO_CLIENTE (
    ID INT AUTO_INCREMENT NOT NULL COMMENT 'Identificador unico del subtipo de cliente',
    TIPO_CLIENTE VARCHAR(15) NOT NULL COMMENT 'Tipo de cliente al que aplica el subtipo: NATURAL o JURIDICO',
    CODIGO VARCHAR(20) NOT NULL COMMENT 'Codigo funcional del segmento',
    NOMBRE VARCHAR(50) NOT NULL COMMENT 'Nombre comercial del segmento',
    DESCRIPCION VARCHAR(255) NULL COMMENT 'Descripcion de las caracteristicas del segmento',
    ESTADO VARCHAR(15) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado del subtipo',
    OBSERVACIONES VARCHAR(255) NULL COMMENT 'Notas internas del segmento',
    FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha de creacion',
    FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Ultima actualizacion del subtipo de cliente',
